        public static void checkTreasure()
        {
            //Used to check if the Hero has found the key and is at the treasure
            //chest, this would end the game as it is currently set up.
            if(moveHold == true)
            {
                jTextArea2.append("You are being attacked and cannot open the ");
                jTextArea2.append("treasure chest.\n");
            }
            else if(map.getTerrain() != 'T')
            {
                jTextArea2.append("You are not at the treasure chest.\n");
            }
            else if(map.getTerrain() == 'T')
            {
                System.out.println("Starting check Treasure");
                System.out.println("Current terrain: " + map.getTerrain());
                //moveHold = true;
                int gold = num.nextInt(1000) + 1;
                int currGold = newHero.getGold();
                
                jTextArea2.append("You have found the treasure chest,\n");
                jTextArea2.append("and claim all of it's contents!\n");
                jTextArea2.append("You found " + gold + " gold in the chest.\n");
                jTextField9.setText(String.valueOf(currGold + gold));
                
            }
        }
        
        public static int awardGold(EnemyClass newEnemy)
        {
            //Awards the gold that was found in the treasure chest by the player
            // to his/her inventory.

            int droppedGold = num.nextInt(newEnemy.maxGold()) + 2;
            int currGold = newHero.getGold();
            newHero.addGold(droppedGold);
            jTextField9.setText(String.valueOf(droppedGold + currGold));
            return droppedGold;

        }

