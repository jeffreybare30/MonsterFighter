        public static void checkIfAttacked()
        {
            //Checks if the player has been attacked by an enemy and displays a 
            // message saying they cannot move.
            if(attacked && moveHold == true)
            {
                if(underAttack == true)
                {
                    jTextArea2.append("You are currently under attack.\n");
                }
                else
                {
                    //Do attack stuff
                    moveHold = true;
                    jTextArea2.append("You have been attacked!\n");

                    attackGame();
                }

            }
        }
        
