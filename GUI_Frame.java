    public static void attackGame()
    {
        //Logic that handles the player attacking the enemy and the enemy
        // attacking the player.
        //jPanel2.setVisible(true);
        if(underAttack == false)
        {
            showEnemy();

            int enemyNum = num.nextInt(99) + 1;
            int enemyNum2;

            if(enemyNum <= 60)
            {
                //Create Imp enemey
                enemyNum2 = 1;
                System.out.println("Creating Imp.");
            }
            else if(enemyNum > 60 && enemyNum <= 93)
            {
                //Create Wolf enemy
                enemyNum2 = 2;
                System.out.println("Creating Wolf");
            }
            else
            {
                //Create Ogre enemy
                enemyNum2 = 3;
                System.out.println("Creating Ogre");
            }

            newEnemy = new EnemyClass(enemyNum2);

            System.out.println("Beginning Enemy Setup");
            setEnemy(newEnemy);
            System.out.println("Enemy Set up");
            underAttack = true;
        }
        else
        {
            jTextArea2.append("You have already been attacked, cannot create ");
            jTextArea2.append("a new enemy at this time.\n");
        }
        
    }
        
